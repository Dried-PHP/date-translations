    private static array $translationsPerLanguage = [];

    private ?array $translations = null;

    private function __construct(
        public readonly string $locale,
    ) {
        if (!isset(self::SUPPORTED_LOCALES[$locale])) {
            throw UnsupportedLocale::fromLocale($locale);
        }
    }

    public static function forLocale(string $locale): self
    {
        return self::$translationsPerLanguage[$locale] ??= new self($locale);
    }

    /** @return list<string> */
    public static function getSupportedLocales(): array
    {
        return array_keys(self::SUPPORTED_LOCALES);
    }

    /** @return array<string, Closure|array|string|int> */
    public function getTranslations(): array
    {
        return $this->translations ??= require __DIR__ . '/../Lang/' . $this->locale . '.php';
    }
